/*------------------------------------------------
  　ライブラリ読み込み
-------------------------------------------------*/
var express = require('express');
var http = require('http');

/*------------------------------------------------
  　HTTPサーバ設定
-------------------------------------------------*/
var app = express();
var server = http.createServer(app);

/*------------------------------------------------
  　WebSocketサーバをリスン状態に
-------------------------------------------------*/
var websocket = require('./js/websocket.js');
websocket.init(server);


/*------------------------------------------------
  　サーバをリスン状態に
-------------------------------------------------*/
server.listen(3000 , '0.0.0.0');
